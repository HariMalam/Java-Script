console.log("Operators in JS");

let a = 45;
let b = 5;

console.log("Arithmetic Operators");
console.log("a + b = ", a+b);
console.log("a - b = ", a-b);
console.log("a * b = ", a*b);
console.log("a / b = ", a/b);
console.log("a ** b = ", a**b);
console.log("a % b = ", a%b);

console.log(a);
console.log("a++ =",a++);
console.log("a-- =",a--);
console.log(a);
console.log("++a =",++a);
console.log("--a =",--a);

console.log("Assignment Operators");

a = 10;
a += 5;
console.log(a);
a -= 5;
console.log(a);
a *= 5;
console.log(a);
a /= 5;
console.log(a);

console.log("Comparison Operators");

let c1 = 6;
let c2 = "6";

console.log(c1 == c2);
console.log(c1 != c2);

console.log(c1 === c2);
console.log(c1 !== c2);
